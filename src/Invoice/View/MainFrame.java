    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        
        Vector result = null;
        InvoiceOperation select = new InvoiceOperation();
        try {
            jTable2.setModel(select.SelectInvoice(jTable1));
        } catch (FileNotFoundException ex) {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        result = select.showSelectInvoice(jTable1);
        jLabel6.setText(" "+result.get(0)+" ");
        jLabel5.setText(""+result.get(3)+"");
        jTextField1.setText((String) result.get(1));
        jTextField2.setText((String) result.get(2));
        
//        select.SelectInvoice(jTable1);
    }//GEN-LAST:event_jTable1MouseClicked

