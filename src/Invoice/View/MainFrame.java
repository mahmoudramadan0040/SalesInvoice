    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        CreateInvoice create =new CreateInvoice();
        int result= JOptionPane.showConfirmDialog(null,create,"Create New Invoice Form ",JOptionPane.OK_CANCEL_OPTION );
        InvoiceCreate createInvoice = new InvoiceCreate(); 
        if(result == JOptionPane.OK_OPTION){
            ArrayList<String> values = create.GetValues();
            try {
                createInvoice.Create(jTable1,values);
            } catch (ParseException ex) {
                Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
           
    }//GEN-LAST:event_jButton1ActionPerformed

